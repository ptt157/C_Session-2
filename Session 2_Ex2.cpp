#include <stdio.h>

int main() {
    // Khai bao va khoi tao hai bien so nguyen
    int a = 5;
    int b = 10;

    // Tinh tong cua hai bien a va b, luu vao bien tong
    int tong = a + b;

    // In ra ket qua
    printf("Tong cua %d va %d la: %d\n", a, b, tong);

    return 0;
}

